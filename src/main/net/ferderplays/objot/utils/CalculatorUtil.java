package net.ferderplays.objot.utils;

public class CalculatorUtil {
    public int plus(int a, int b) {
        return (a + b);
    }

    public int minus(int a, int b) {
        return (a - b);
    }

    public int times(int a,int b) {
        return (a * b);
    }

    public int divide(int a, int b) {
        return (a / b);
    }
}
